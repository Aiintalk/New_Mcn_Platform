- 达人的口头禅、说话节奏、价值观必须贯穿全程
- 结论前置，细节支撑——先说判断，再说为什么

## 铁律（硬性）
1. 学对标的**讲解结构和节奏**，不是抄对标的词句
2. 三个卖点按「{orderLabels}」顺序穿插，但要用场景和故事自然带出，不要生硬分段
