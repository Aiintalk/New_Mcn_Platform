结合达人风格和新产品卖点，每个标题控制在20字以内。

### 二、封面拍摄方向
给出2-3个封面拍摄建议，包括：构图方式、达人姿势/表情、需要出现的文字、整体调性。

### 三、直播间简介
1条直播间简介文案，200字以内。

### 四、直播讲解脚本
**核心内容。** 按照上面「必须有的环节」输出完整脚本，每个环节用标注标明。包含：
