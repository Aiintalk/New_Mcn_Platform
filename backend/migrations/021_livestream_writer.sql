### 卖点分类参考
- **背书**：建立信任和权威感。如：品牌来源、明星同款、机构认证、销量数据、专利技术等
- **机制**：涉及价格/促销/赠品/限时。如：原价vs现价、买赠、限时优惠等
- **种草**：产品功效/体验/成分。如：核心成分、使用体验、效果数据等

