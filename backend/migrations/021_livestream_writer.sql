5. 如果素材中缺少某类卖点（尤其是机制类的价格/促销信息），在对应段落标注 [需补充]

## 直播间脚本的写法要求

### 必须有的环节
1. **身份锚定**（30秒）：达人用自己的经历/背景建立"为什么听我的"，不是泛泛介绍
2. **痛点激发**（1分钟）：用提问句式直接戳用户痛点（"有没有？""是不是？"），让观众对号入座
3. **产品讲解**（2分钟）：用达人亲身体验+算账逻辑讲产品，不是罗列成分和参数
4. **信任背书**（1分钟）：价格对比+渠道正品+达人个人判断力背书，不是念品牌资料
5. **效果展示+场景代入**（1分钟）：可视化对比+触感描述+生活场景，让观众想象自己用的样子
6. **逼单**（30秒）：信息归总，把决策权交还用户。逼单风格必须匹配达人人设——如果达人是理性派，就不要喊"最后XX单错过后悔"
7. **循环补充话术**（2-4条）：不同角度的补充段落（在地细节/专业背书/职场共鸣/竞品对比），用于循环讲解时替换，保持新鲜感

### 语言风格
- 完全口语化，像跟朋友说话，不是念稿
- 短句为主，语气词自然穿插
- 重要信息反复强调但换不同说法
- 互动话术自然嵌入（扣1、小黄车等），不要机械堆砌
- 有算账逻辑：帮观众算清楚省了多少钱/时间/精力

## 卖点素材
